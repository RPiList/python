# Die FOR-Schleife und Arrays

# Die Variable "einkaufen" ist ein Array.
# Stell Dir das wie eine Kiste vor, in die Du 
# mehrere Sachen packen kannst.

einkaufen = ["Joghurt", "Nutella", "Bananen", "Pommes"]

# um die Punkte in dem Array "einkaufen" 
# alle durchzulaufen, brauchen wir eine FOR-Schleife

for produkt in einkaufen:
    print("Wir brauchen " + produkt)
