# Die IF oder WENN Abfrage
# Die IF Abfrage wird benutzt, wenn man 
# einen Befehl nur unter bestimmten Umständen
# ausführen möchte

a = 6
if a == 5:
    print("a ist kleiner als 5")
else:
    print("a ist größer als 5")

# Der Befehl print() wird nur ausgefügt,
# wenn die Bedingung "a kleiner 5" wahr ist.

